    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txt_searchInput.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mã sv cần tìm!", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            table_main.clearSelection();
            try {
                int t = gradeDAO.search(txt_searchInput.getText().trim());
                if (t == -1) {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên hoặc sinh viên chưa có điểm!", "Message", JOptionPane.ERROR_MESSAGE);
                    txt_masv.requestFocus();
                } else {
                    Grade gr = gradeDAO.get(t);
                    txt_hoten.setText(gr.getHoTen());
                    txt_masv.setText(gr.getMaSV());
                    txt_ta.setText(String.valueOf(gr.getTiengAnh()));
                    txt_th.setText(String.valueOf(gr.getTinHoc()));
                    txt_gdtc.setText(String.valueOf(gr.getGDTC()));
                    txt_tb.setText(String.valueOf(gr.getTB()));
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (txt_masv.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập mã sv vào input để xóa!", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            int t = JOptionPane.showConfirmDialog(this, "Bạn có chắc xóa không?", "Message", JOptionPane.INFORMATION_MESSAGE);
            if (t == 0) {
                try {
                    gradeDAO.delete(txt_masv.getText());
                    ArrayList<Grade> list = gradeDAO.getTop3();
                    if (!list.isEmpty()) {
                        loadToTable(list);
                        setGradeToInput(0);
                    }
                    JOptionPane.showMessageDialog(this, "Xóa điểm thành công!", "Message", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Message", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

