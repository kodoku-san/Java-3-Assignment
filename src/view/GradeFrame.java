    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (txt_masv.getText().trim().equals("")
                || txt_ta.getText().trim().equals("")
                || txt_th.getText().trim().equals("")
                || txt_gdtc.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Hãy nhập tất cả thông tin!", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Grade grs = setInputToGrade();
                if (addCheck(grs)) {
                    if (grs != null) {
                        gradeDAO.insert(grs);
                        ArrayList<Grade> list = gradeDAO.getTop3();
                        if (!list.isEmpty()) {
                            loadToTable(list);
                            setGradeToInput(0);
                        }
                        JOptionPane.showMessageDialog(this, "Thêm điểm thành công!", "Message", JOptionPane.INFORMATION_MESSAGE);

                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

