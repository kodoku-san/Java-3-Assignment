    public void gradeSet(Grade grade) throws Exception{
        try {
            txt_gdtc.setText(String.valueOf(grade.getGDTC()));
            txt_ta.setText(String.valueOf(grade.getTiengAnh()));
            txt_th.setText(String.valueOf(grade.getTinHoc()));
            txt_tb.setText(String.valueOf(grade.getTB()));
            txt_masv.setText(grade.getMaSV());
            txt_hoten.setText(grade.getHoTen());
        } catch (Exception e) {
            throw new Exception(e.getMessage());
        }
    }
    
    public Grade setInputToGrade() {
        try {
            Grade grade = new Grade();
            grade.setMaSV(txt_masv.getText());
            grade.setTiengAnh(Double.parseDouble(txt_ta.getText()));
            grade.setTinHoc(Double.parseDouble(txt_th.getText()));
            grade.setGDTC(Double.parseDouble(txt_gdtc.getText()));
            return grade;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Message", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }

    public boolean addCheck(Grade gr) throws Exception {
        if (gr == null) {
            return false;
        }
        try {
            for (var grade : gradeDAO.get()) {
                if (grade.getMaSV().equals(gr.getMaSV())) {
                    throw new Exception("Sinh viên \"" + grade.getMaSV() + "\" đã có điểm rồi!");
                }
            }
            return true;
        } catch (Exception e) {
            throw new Exception(e.getMessage());
        }
    }


