    public void loadListToTable(ArrayList<Students> list) {
        model.fireTableDataChanged(); // fix: WARNING: row index is bigger than sorter's row count   
        model.setRowCount(0);
        for (Students s : list) {
            model.addRow(new Object[]{
                s.getMaSV(),
                s.getHoTen(),
                s.getEmail(),
                s.getSoDT(),
                rGT(s.getGioiTinh()),
                s.getDiaChi(),
                s.getHinhAnh()
            });
        }
    }

    public String rGT(int t) {
        if (t == 1) {
            return "Nam";
        }
        return "Nữ";
    }

