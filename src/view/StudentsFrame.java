    public Students getStudent() throws Exception {
        Students s = new Students();
        s.setMaSV(txt_masv.getText());
        s.setHoTen(txt_hoten.getText());
        s.setEmail(txt_email.getText());
        s.setSoDT(txt_sdt.getText());
        s.setDiaChi(txt_diachi.getText());
        s.setHinhAnh(pathLblIcon);
        int gt;
        if (rdo_nam.isSelected()) {
            gt = 1;
        } else {
            gt = 0;
        }
        s.setGioiTinh(gt);
        return s;
    }

    public boolean checkAll() {
        if (txt_masv.getText().trim().equals("")
                || txt_hoten.getText().trim().equals("")
                || txt_email.getText().trim().equals("")
                || txt_sdt.getText().trim().equals("")
                || txt_diachi.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tất cả thông tin", "Message", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (txt_sdt.getText().length() < 10 || txt_sdt.getText().length() > 13) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập sđt từ 10-13 kí tự", "Message", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!rdo_nam.isSelected() && !rdo_nu.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn giới tính", "Message", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (lbl_img.getIcon() == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hình đại diện", "Message", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }

    public void resetAll() {
        table_main.clearSelection();
        txt_masv.setText("");
        txt_hoten.setText("");
        txt_email.setText("");
        txt_sdt.setText("");
        txt_diachi.setText("");
        buttonGroup1.clearSelection();
        lbl_img.setIcon(null);
        lbl_img.setText("Chọn ảnh đại diện");
    }

    public void setStudentToInput(int t) {
        table_main.setRowSelectionInterval(t, t);

        txt_masv.setText(table_main.getValueAt(t, 0).toString());
        txt_hoten.setText(table_main.getValueAt(t, 1).toString());
        txt_email.setText(table_main.getValueAt(t, 2).toString());
        txt_sdt.setText(table_main.getValueAt(t, 3).toString());
        txt_diachi.setText(table_main.getValueAt(t, 5).toString());

        String gt = table_main.getValueAt(t, 4).toString();
        String img = table_main.getValueAt(t, 6).toString();
        lbl_img.setIcon(ResizeImage("src\\img\\" + img));
        pathLblIcon = img;
        if (gt.equals("Nam")) {
            rdo_nam.setSelected(true);
        } else {
            rdo_nu.setSelected(true);
        }
    }

    public ImageIcon ResizeImage(String ImagePath) {
        lbl_img.setText("");
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(lbl_img.getWidth(), lbl_img.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }


